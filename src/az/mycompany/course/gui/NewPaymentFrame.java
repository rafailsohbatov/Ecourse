    private void showStudentData() {
        try {
            List<Student> listStudent = studentService.getStudentList();
            DefaultComboBoxModel model = (DefaultComboBoxModel) studentCmb.getModel();
            for (Student student : listStudent) {
                model.addElement(student);
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }

    private void showTeacherData(List<Teacher> teacherlist) {
        try {
            DefaultComboBoxModel model = (DefaultComboBoxModel) teacherCmb.getModel();
            model.addElement("Select Teacher");
            model.removeAllElements();
            for (Teacher teacher : teacherlist) {
                model.addElement(teacher);
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }

    private void showLessonData(List<Lesson> lessonList) {
        try {
            DefaultComboBoxModel model = (DefaultComboBoxModel) lessonCmb.getModel();
            model.removeAllElements();
            model.addElement("Select Lesson");
            for (Lesson lesson : lessonList) {
                model.addElement(lesson);
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }
