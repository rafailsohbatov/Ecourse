    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        try {
            Student student = (Student) studentCmb.getSelectedItem();
            Teacher teacher = (Teacher) teacherCmb.getSelectedItem();
            Lesson lesson = (Lesson) lessonCmb.getSelectedItem();
            Float amount = Float.valueOf(amountTxt.getText());
            TeacherLesson teacherLesson = teacherService.getTeacherLessonByTeacherAndLessonId(teacher.getId(), lesson.getId());
            StudentTeacherLesson studentTeacherLesson = studentService.getStudentTeacherLessonById(student.getId(), teacherLesson.getId());
            Payment payment = new Payment();
            payment.setStudentTeacherLesson(studentTeacherLesson);
            payment.setAmount(amount);
            paymentService.addPayment(payment);
            JOptionPane.showMessageDialog(null, "Successfully added");
            this.dispose();
        } catch (Exception ex) {
            ex.printStackTrace();
        }

    }//GEN-LAST:event_saveBtnActionPerformed

